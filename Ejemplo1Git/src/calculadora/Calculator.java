package calculadora;
public class Calculator {
	 public double add(double number1, double number2){
	 return number1 + number2;
	 }
	 public double subtract(double number1, double number2){
	 return number1 - number2;
	 }
	 public double multiply(double number1, double number2) {
	 return number1 * number2;
	 }
	 public double divide(double number1, double number2){
	 return number1 / number2;
	 }
	 public boolean esMayor (double number1, double number2)
	 {
		 return number1>number2;
	 }
	 public boolean esMenor (double number1, double number2)
	 {
		 return number1<number2;
	 }
	 public boolean esIgual (double number1, double number2)
	 {
		 return number1==number2;
	 }
	 public boolean compMayorIgual (double number1, double number2)
	 {
		 return number1>=number2;
	 }
	 
	 
	}
